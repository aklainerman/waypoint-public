// Hill Ops tab AND, when the target is a Hill member, opens the
// matching side drawer so the user lands with context. Falls back to
// the legacy summary-search behavior for non-member targets (e.g.
// committees, ad-hoc engagements).
//
// Called from the MC Hill list onclick handler (mission-control.js).
// hillBioguide is threaded via activateTab opts so the an earlier refactor
// 'washops' case in tabs.js can fire openHillMemberDrawer right after
// the renderers populate.
window._v230GoToHillEntry = function (targetType, targetId, summary) {
  if (targetType === 'member' && targetId) {
    if (typeof activateTab === 'function') {
      activateTab('washops', { hillBioguide: targetId });
    }
    return;
  }
  // Non-member target (committee, ad-hoc, etc.): fall back to legacy
  // search-by-summary behavior.
  if (typeof window._v98GoToHill === 'function') {
    window._v98GoToHill(summary || '');
  }
};

// activateTab is invoked. Watches .tab-btn class changes.
(function v100SyncRail() {
  const TAB_LABELS = {
    dashboard: ['OPS', 'Mission Control'],
    solicitations: ['OPS', 'Pipeline'],
    letters: ['OPS', 'Support'],
    washops: ['OPS', 'Hill Ops'],
    offices: ['INTEL', 'Orgs'],
    contacts: ['INTEL', 'Contacts'],
    budget: ['INTEL', 'Budget'],
    scout: ['INTEL', 'Scout'],
  };
  function sync() {
    const active = document.querySelector('.tab-btn.active');
    if (!active) return;
    const tab = active.dataset.tab;
    document.querySelectorAll('.v98-rail-link[data-v98-tab]').forEach(function (el) {
      el.classList.toggle('active', el.dataset.v98Tab === tab);
    });
    const labels = TAB_LABELS[tab];
    if (labels) {
      const grp = document.querySelector('#v98Crumb > span');
      const leaf = document.getElementById('v98CrumbLeaf');
      if (grp) grp.textContent = labels[0];
      if (leaf) leaf.textContent = labels[1];
    }
  }
  function attach() {
    const bar = document.getElementById('tabbar');
    if (!bar) { setTimeout(attach, 100); return; }
    new MutationObserver(sync).observe(bar, {
      attributes: true, attributeFilter: ['class'], subtree: true
    });
    sync();
  }
  if (document.readyState !== 'loading') attach();
  else document.addEventListener('DOMContentLoaded', attach);
})();

