function wireChipJumps(scope) {
  scope.querySelectorAll('[data-office-jump]').forEach(a => a.addEventListener('click', (e) => {
    e.stopPropagation();
    activateTab('offices');
    document.getElementById('officesSearch').value = (DB.get('offices', a.dataset.officeJump)||{}).name || '';
    renderOffices();
  }));
  scope.querySelectorAll('[data-contact-jump]').forEach(a => a.addEventListener('click', (e) => {
    e.stopPropagation();
    activateTab('contacts');
    const c = DB.get('contacts', a.dataset.contactJump);
    document.getElementById('contactsSearch').value = c ? ((c.firstName||'') + ' ' + (c.lastName||'')).trim() : '';
    renderContacts();
  }));
  scope.querySelectorAll('[data-legislator-jump]').forEach(a => a.addEventListener('click', (e) => {
    e.stopPropagation();
    const bg = a.dataset.legislatorJump;
    const m  = legislatorById(bg);
    if (typeof activateTab === 'function') activateTab('washops');
    const sub = document.querySelector('.subtab-btn[data-subtab="washops-members"]');
    if (sub) sub.click();
    const search = document.getElementById('hillMSearch');
    if (search && m) {
      search.value = m.full_name || m.last_name || '';
      try { search.dispatchEvent(new Event('input', { bubbles: true })); } catch (_) {}
    }
  }));
}

