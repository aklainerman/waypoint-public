// js/nav/drillthrough.js
//
// Drill-through helpers + left-rail sync. Three previously-adjacent
// (but non-contiguous) clusters in the inline monolith, all serving
// the same purpose: "user clicks something here -> we navigate them to
// the right tab with the right filter applied, and we keep the left
// rail's active-state and counts in sync."
//
// Cluster contents (~135 lines extracted from index.html in v196):
//
//   * Mission Control drill helpers (v100):
//       window._v98GoToSol(titleQuery)   -> Pipeline tab, filter by title
//       window._v98GoToStage(stage)      -> Pipeline tab, filter by stage
//       window._v98GoToOrg(nameQuery)    -> Orgs tab, filter by name
//       window._v98GoToHill(query)       -> Hill Ops tab, filter by summary
//     These are referenced as inline `onclick="_v98GoTo*(...)"`
//     attributes generated by js/render/mission-control.js, so they
//     must remain on the global window object.
//
//   * v100SyncRail IIFE -- a MutationObserver on #tabbar that keeps the
//     left-rail "active" class and the v98Crumb breadcrumb labels in
//     sync with whichever .tab-btn currently has .active. Polls for
//     #tabbar via setTimeout so it survives module-deferred load.
//
//   * refreshTabCounts() -- updates both the v98 rail counts
//     (.v98-rail-count[data-count]) and legacy tab counts
//     (.tab-count[data-count]) from DB.state. v126 / v128 special-case
//     for "washops" (sums hill_meetings + hill_requests instead of the
//     empty washops table).
//
//   * wireChipJumps(scope) -- delegates click handling for chip elements
//     in a given container (typically a <tbody>) with:
//       data-office-jump      -> activate Orgs, filter to that office
//       data-contact-jump     -> activate Contacts, filter to contact
//       data-legislator-jump  -> activate WashOps -> Members, filter
//
// Originally at file-scope of the inline monolith; lifted to ES module
// in v196. Same classic-script-split pattern as v181-v195.
//
// Pre-extraction audit (v185 pattern). 6 names need window exposure:
//   _v98GoToSol, _v98GoToStage, _v98GoToOrg, _v98GoToHill
//                     -- already on window via direct assignment; the
//                        original `window.X = function () {...}` lines
//                        are preserved verbatim, so no extra footer
//                        line is needed for these.
//   refreshTabCounts  -- called from monolith refreshAll() (~24594).
//                        Once refreshTabCounts lives in this module,
//                        refreshAll's call becomes external; expose.
//   wireChipJumps     -- called from contacts.js, lets.js, sols.js
//                        (3 extracted modules). Expose unconditionally.
//
// Module-internal only (NOT exposed):
//   The v100SyncRail IIFE is anonymous and self-invoking; nothing
//   outside the IIFE needs it.
//
// External file-scope refs the block consumes:
//   DB                  -- already on window (major global, no shim)
//   (audit clean -- no other file-scope const / let consumed)
//
// External function calls (all auto-hoisted to window by their
// top-level `function` decls in the classic-script monolith body):
//   activateTab        (drill helpers + IIFE + wireChipJumps)
//   legislatorById     (wireChipJumps legislator-jump handler)
//   renderOffices      (wireChipJumps office-jump handler)
//   renderContacts     (wireChipJumps contact-jump handler)

window._v98GoToSol = function (titleQuery) {
  if (typeof activateTab === 'function') activateTab('solicitations');
  setTimeout(function () {
    const s = document.getElementById('solSearch');
    if (s) { s.value = titleQuery || ''; s.dispatchEvent(new Event('input')); }
  }, 30);
};
