// Tab counts (rendered alongside tab labels)
function refreshTabCounts() {
  document.querySelectorAll('.v98-rail-count[data-count]').forEach(el => {
    const t = el.dataset.count;
    if (!(window.DB && DB.state)) return;
    if (t === 'washops') {
      const meets = (DB.state.hill_meetings || []).length;
      const reqs  = (DB.state.hill_requests || []).length;
      el.textContent = (meets + reqs);
    } else if (Array.isArray(DB.state[t])) {
      el.textContent = DB.state[t].length;
    }
  });
  document.querySelectorAll('.tab-count[data-count]').forEach(el => {
    // of engagement activity across all members + committees instead of the
    // useless 0 from washops. Other tabs keep their original behavior.
    if (el.dataset.count === 'washops') {
      const meets = (DB.state.hill_meetings || []).length;
      const reqs  = (DB.state.hill_requests || []).length;
      el.textContent = (meets + reqs);
    } else {
      el.textContent = (DB.state[el.dataset.count] || []).length;
    }
  });
  document.querySelectorAll('.v98-rail-count[data-count]').forEach(el => {
    if (el.dataset.count === 'washops') {
      const meets = (DB.state.hill_meetings || []).length;
      const reqs  = (DB.state.hill_requests || []).length;
      el.textContent = (meets + reqs);
    }
  });
}


